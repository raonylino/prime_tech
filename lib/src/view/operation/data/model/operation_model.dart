import 'package:prime_pronta_resposta/src/model/contact_model.dart';
import 'package:prime_pronta_resposta/src/view/operation/domain/entities/operation_entity.dart';

class OperationModel extends OperationEntity {
  OperationModel({
    required super.id,
    required super.idCompany,
    required super.sequence,
    required super.idServiceType,
    required super.idServiceStatus,
    required super.identification,
    required super.idOccurrenceType,
    required super.goods,
    required super.shippingCompanyName,
    required super.cep,
    required super.address,
    required super.number,
    required super.complement,
    required super.neighborhood,
    required super.city,
    required super.uf,
    required super.referencePoint,
    required super.latitude,
    required super.longitude,
    required super.contacts,
    required super.observation,
    required super.idUserCreate,
    required super.createdAt,
    required super.updatedAt,
  });
  factory OperationModel.fromJson(Map<String, dynamic> json) {
    return OperationModel(
      id: json['id'],
      idCompany: json['id_company'],
      sequence: json['sequence'],
      idServiceType: json['id_service_type'],
      idServiceStatus: json['id_service_status'],
      identification: json['identification'],
      idOccurrenceType: json['id_occurrence_type'],
      goods: List<String>.from(json['goods'].map((x) => x)),
      shippingCompanyName: json['shipping_company_name'],
      cep: json['cep'],
      address: json['address'],
      number: json['number'],
      complement: json['complement'],
      neighborhood: json['neighborhood'],
      city: json['city'],
      uf: json['uf'],
      referencePoint: json['reference_point'],
      latitude: json['latitude'],
      longitude: json['longitude'],
      contacts: List<Contact>.from(
        json["contacts"].map((x) => Contact.fromJson(x)),
      ),
      observation: json['observation'],
      idUserCreate: json['id_user_create'],
      createdAt: json['created_at'],
      updatedAt: json['updated_at'],
    );
  }
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'id_company': idCompany,
      'sequence': sequence,
      'id_service_type': idServiceType,
      'id_service_status': idServiceStatus,
      'identification': identification,
      'id_occurrence_type': idOccurrenceType,
      'goods': List<dynamic>.from(goods.map((x) => x)),
      'shipping_company_name': shippingCompanyName,
      'cep': cep,
      'address': address,
      'number': number,
      'complement': complement,
      'neighborhood': neighborhood,
      'city': city,
      'uf': uf,
      'reference_point': referencePoint,
      'latitude': latitude,
      'longitude': longitude,
      'contacts': List<dynamic>.from(contacts.map((x) => x.toJson())),
      'observation': observation,
      'id_user_create': idUserCreate,
      'created_at': createdAt,
      'updated_at': updatedAt,
    };
  }
}
